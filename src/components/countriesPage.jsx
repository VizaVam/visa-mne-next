const LazySection = ({ children, fallback = "Загрузка...", rootMargin = "100px" }) => {
    const { ref, inView } = useInView({
        triggerOnce: true,
        rootMargin,
    });

    return (
        <div ref={ref}>
            {inView ? children : <div className="py-10 text-center">{fallback}</div>}
        </div>
    );
};

